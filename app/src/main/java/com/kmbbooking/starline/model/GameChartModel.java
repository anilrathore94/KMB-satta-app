public class GameChartModel {


    String game_name;
    String result_date;
    String open_number;
    String close_number;

    public String getGame_name() {
        return game_name;
    }

    public void setGame_name(String game_name) {
        this.game_name = game_name;
    }

    public String getResult_date() {
        return result_date;
    }

    public void setResult_date(String result_date) {
        this.result_date = result_date;
    }

    public String getOpen_number() {
        return open_number;
    }

    public void setOpen_number(String open_number) {
        this.open_number = open_number;
    }

    public String getClose_number() {
        return close_number;
    }

    public void setClose_number(String close_number) {
        this.close_number = close_number;
    }
}
